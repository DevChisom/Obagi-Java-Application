public class DisplayNumbers {
    public static void main(String[] args) {
        // a) Using one println statement
        System.out.println("1 2 3 4");

        // b) Using four print statements
        System.out.print("1 ");
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4\n"); // \n to move to the next line

        // c) Using one printf statement
        System.out.printf("%d %d %d %d%n", 1, 2, 3, 4);
    }
}




